public class PositivoNegativo {
    public static void main(String[] args) {
        int numero = 13;
        System.out.println(numero);

        numero = - numero;
        System.out.println(numero);

        numero = + numero;
        System.out.println(numero);

        numero = numero * -1;
        System.out.println(numero);
    }
}
