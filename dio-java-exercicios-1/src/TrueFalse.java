public class TrueFalse {
    public static void main(String[] args) {
        boolean sentenca = true;
        System.out.println(sentenca);

        sentenca = !sentenca;
        System.out.println(sentenca);

        sentenca = false;
        System.out.println(sentenca);

        sentenca = !false;
        System.out.println(sentenca);
    }
}
